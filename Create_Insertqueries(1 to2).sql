-- --------------------------------------1) Table Creation ----------------------------------------------------- 
create table supplier(SUPP_ID INT NOT NULL,SUPP_NAME VARCHAR(50) not null, 
SUPP_CITY  VARCHAR(50) not null,SUPP_PHONE VARCHAR(50) not null,
PRIMARY KEY(SUPP_ID));



create table customer(CUS_ID INT NOT NULL,CUS_NAME VARCHAR(50) not null, 
CUS_PHONE  VARCHAR(50) not null,CUS_CITY VARCHAR(50) not null,CUS_GENDER char,
PRIMARY KEY (CUS_ID));



create table category(CAT_ID INT NOT NULL,CAT_NAME VARCHAR(50) not null,
PRIMARY KEY (CAT_ID));



CREATE TABLE IF NOT EXISTS product (
    PRO_ID INT NOT NULL,
    PRO_NAME VARCHAR(20) NOT NULL DEFAULT 'Dummy',
    PRO_DESC VARCHAR(60),
    CAT_ID INT NOT NULL,
    PRIMARY KEY (PRO_ID),
    FOREIGN KEY (CAT_ID)
        REFERENCES CATEGORY (CAT_ID)
);



CREATE TABLE IF NOT EXISTS supplier_pricing (
PRICING_ID INT NOT NULL,
PRO_ID INT NOT NULL,
SUPP_ID INT NOT NULL,
SUPP_PRICE INT DEFAULT 0,
PRIMARY KEY (PRICING_ID),
FOREIGN KEY (PRO_ID) REFERENCES PRODUCT (PRO_ID),
FOREIGN KEY (SUPP_ID) REFERENCES SUPPLIER(SUPP_ID)
);
SHOW TABLES;

CREATE TABLE IF NOT EXISTS `ORDER`(
ORD_ID INT NOT NULL,
ORD_AMOUNT INT NOT NULL,
ORD_DATE DATE NOT NULL,
CUS_ID INT NOT NULL,
PRICING_ID INT NOT NULL,
PRIMARY KEY(ORD_ID),
FOREIGN KEY(CUS_ID) REFERENCES CUSTOMER(CUS_ID),
FOREIGN KEY(PRICING_ID) REFERENCES supplier_pricing(PRICING_ID)
);

CREATE TABLE IF NOT EXISTS RATING(
RAT_ID INT NOT NULL,
ORD_ID INT NOT NULL,
RAT_RATSTARS INT NOT NULL,
PRIMARY KEY(RAT_ID),
FOREIGN KEY(ORD_ID) REFERENCES `ORDER`(ORD_ID));

